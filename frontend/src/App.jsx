import { useMemo, useRef, useState } from 'react'
import Sidebar from './components/Sidebar'
import MobileDocumentStrip from './components/MobileDocumentStrip'
import AnswerCard from './components/AnswerCard'
import EmptyState from './components/EmptyState'
import InputBar from './components/InputBar'
import SourcesPanel from './components/SourcesPanel'
import NotesPanel from './components/NotesPanel'
import { pickMockExchange } from './mockData'
import { queryBackend, uploadFile, isBackendConfigured } from './api'

// Real documents come from actual uploads (see handleFilesAdded) — start
// empty so the sidebar reflects what's really been uploaded this session,
// not a hardcoded demo set left over from early UI development.
const INITIAL_DOCS = []

const NAV_TABS = ['Focus', 'Resources', 'Notes']

// STATUS_LABEL / description text shown on each Resources-tab card, keyed
// off the real upload lifecycle in `documents` (see handleFilesAdded) —
// replaces the old hardcoded relevance/description copy from the mock
// SOURCES array.
const STATUS_LABEL = {
  ready: 'READY',
  processing: 'PROCESSING…',
  error: 'UPLOAD FAILED',
}

const STATUS_DESCRIPTION = {
  ready: 'Indexed and available to the assistant.',
  processing: 'Still being read and indexed — hang tight.',
  error: 'Something went wrong ingesting this file. Try re-uploading.',
}

/**
 * Demo shell wiring together the full flow: ask -> thinking -> answer/empty,
 * plus the Resources tab (multi-document source selection) and the mobile
 * horizontal bookshelf. Replace the mocked response in `handleAsk` with a
 * real call to the FastAPI backend when it's ready.
 */
export default function App() {
  const [activeTab, setActiveTab] = useState('Focus')
  const [activeDocId, setActiveDocId] = useState(null)
  const [webSearchEnabled, setWebSearchEnabled] = useState(false)
  const [thinking, setThinking] = useState(false)
  // Start on the real empty state, not the hardcoded demo answer — the
  // demo exchange was only ever meant as an early UI placeholder, and
  // showing it on load now that the real backend is wired up makes a
  // genuine first load look like a faked/mocked response.
  const [exchange, setExchange] = useState(null)
  const [showEmpty, setShowEmpty] = useState(true)
  const [documents, setDocuments] = useState(INITIAL_DOCS)
  // Stable per-session id, required by Vanshi's backend to key conversation
  // memory (backend/rag.py: SESSIONS dict, keyed by this). Generated once on
  // mount and regenerated by "New Research" so that starts a fresh backend
  // conversation too, not just a fresh-looking UI.
  const [sessionId, setSessionId] = useState(() => crypto.randomUUID())
  // Hidden <input type="file"> triggered by the Resources tab's "Add
  // Resource" button — reuses the exact same handleFilesAdded pipeline
  // (and therefore the same real /upload call) as the Sidebar's UploadZone,
  // just without duplicating its full drag-and-drop UI in a button context.
  const resourceInputRef = useRef(null)

  // Real Resources-tab cards, derived from the same `documents` state the
  // Sidebar renders — replaces the old hardcoded mock SOURCES array so this
  // tab actually reflects what the user has uploaded this session.
  const sources = useMemo(
    () =>
      documents.map((doc) => ({
        id: doc.id,
        type: doc.type,
        title: doc.name,
        description: STATUS_DESCRIPTION[doc.status] ?? '',
        relevance: STATUS_LABEL[doc.status] ?? doc.status?.toUpperCase() ?? '',
        sourceLabel: `${doc.type === 'pdf' ? 'PDF' : 'IMAGE'} SOURCE`,
      })),
    [documents]
  )

  // Called by UploadZone (via Sidebar) whenever the user drops/picks files.
  // If VITE_API_URL isn't set, uses the fake processing->ready timeout
  // (expected/normal — no backend to call yet). If it IS set, tries the
  // real /upload endpoint and shows a genuine 'error' status on failure
  // instead of masking it — a real, configured backend failing is a bug
  // worth seeing during integration testing, not something to fake past.
  function handleFilesAdded(files) {
    const newDocs = files.map((file) => ({
      id: crypto.randomUUID(),
      file,
      name: file.name,
      type: file.type === 'application/pdf' ? 'pdf' : 'image',
      status: 'processing',
    }))
    setDocuments((prev) => [...prev, ...newDocs])

    newDocs.forEach(async (doc) => {
      if (isBackendConfigured()) {
        try {
          const result = await uploadFile(doc.file)
          setDocuments((prev) =>
            prev.map((d) => (d.id === doc.id ? { ...d, status: 'ready', file_id: result.file_id } : d))
          )
        } catch (err) {
          console.warn(`[studymate] upload of "${doc.name}" failed:`, err.message)
          setDocuments((prev) =>
            prev.map((d) => (d.id === doc.id ? { ...d, status: 'error' } : d))
          )
        }
        return
      }

      // No backend configured at all — this is the expected pre-integration
      // state, so fake the ready transition rather than showing an error.
      setTimeout(() => {
        setDocuments((prev) =>
          prev.map((d) => (d.id === doc.id ? { ...d, status: 'ready' } : d))
        )
      }, 1200 + Math.random() * 800)
    })
  }

  async function handleAsk(question) {
    setActiveTab('Focus')
    setShowEmpty(false)
    setThinking(true)

    // If VITE_API_URL is set, try Vanshi's real /query endpoint first.
    // Any failure — not configured, network error, timeout, bad shape —
    // falls through to the mock so the demo never breaks on a flaky or
    // half-built backend. Only a successful, correctly-shaped response
    // ever displays as if it were real.
    if (isBackendConfigured()) {
      try {
        const readyFileIds = documents
          .filter((d) => d.status === 'ready' && d.file_id)
          .map((d) => d.file_id)
        const result = await queryBackend(question, webSearchEnabled, sessionId, readyFileIds)
        setThinking(false)
        if (!result) {
          setShowEmpty(true)
        } else {
          setExchange({ ...result, question })
        }
        return
      } catch (err) {
        console.warn('[studymate] backend query failed, falling back to mock:', err.message)
        // fall through to mock below
      }
    }

    // Mock path — used when no backend is configured yet, or as a fallback.
    setTimeout(() => {
      setThinking(false)
      const matched = pickMockExchange(question, webSearchEnabled)
      if (!matched) {
        setShowEmpty(true)
      } else {
        setExchange({ ...matched, question })
      }
    }, 1400)
  }

  return (
    <div className="flex h-screen w-full relative overflow-hidden">
      <Sidebar
        documents={documents}
        activeDocId={activeDocId}
        onSelectDocument={setActiveDocId}
        onFilesAdded={handleFilesAdded}
        onNewResearch={() => {
          setShowEmpty(true)
          setActiveTab('Focus')
          setExchange(null)
          setSessionId(crypto.randomUUID())
        }}
      />

      <main className="flex-1 flex flex-col relative overflow-hidden">
        <header className="h-16 w-full max-w-[1100px] mx-auto flex items-center justify-between px-page-margin shrink-0">
          <div className="flex items-center gap-8">
            {NAV_TABS.map((tab) => (
              <button
                key={tab}
                onClick={() => setActiveTab(tab)}
                className={`font-label-mono text-label-mono pb-1 transition-colors ${
                  activeTab === tab
                    ? 'text-primary border-b-2 border-primary'
                    : 'text-on-surface-variant hover:text-primary'
                }`}
              >
                {tab}
              </button>
            ))}
          </div>
        </header>

        <MobileDocumentStrip
          documents={documents}
          activeDocId={activeDocId}
          onSelectDocument={setActiveDocId}
          onFilesAdded={handleFilesAdded}
        />

        <div className="flex-1 overflow-y-auto pb-64 md:pb-48">
          {activeTab === 'Resources' ? (
            <>
              <input
                ref={resourceInputRef}
                type="file"
                accept=".pdf,.jpg,.jpeg,.png,.webp"
                multiple
                className="hidden"
                onChange={(e) => {
                  if (e.target.files?.length) handleFilesAdded(Array.from(e.target.files))
                  e.target.value = '' // allow re-selecting the same file later
                }}
              />
              <SourcesPanel
                sources={sources}
                activeSourceId={activeDocId}
                onSelectSource={setActiveDocId}
                onAddResource={() => resourceInputRef.current?.click()}
              />
            </>
          ) : activeTab === 'Notes' ? (
            <NotesPanel />
          ) : (
            <div className="max-w-[800px] mx-auto px-page-margin pt-8 relative">
              {showEmpty ? <EmptyState /> : <AnswerCard exchange={{ ...exchange, thinking }} />}
            </div>
          )}
        </div>

        <InputBar
          onSubmit={handleAsk}
          thinking={thinking}
          webSearchEnabled={webSearchEnabled}
          onToggleWebSearch={setWebSearchEnabled}
        />
      </main>

      <aside className="w-16 h-full flex flex-col items-center py-8 gap-8 border-l border-outline-variant/10 z-20 shrink-0 hidden lg:flex">
        <div className="w-px h-16 bg-gradient-to-b from-transparent to-outline-variant/30" />
        <div className="flex flex-col gap-4">
          <div className="w-2 h-2 rounded-full bg-primary shadow-[0_0_8px_rgba(255,217,167,0.5)]" />
          <div className="w-1.5 h-1.5 rounded-full bg-outline-variant/40 hover:bg-primary transition-colors cursor-pointer" />
          <div className="w-1.5 h-1.5 rounded-full bg-outline-variant/40 hover:bg-primary transition-colors cursor-pointer" />
        </div>
      </aside>
    </div>
  )
}
